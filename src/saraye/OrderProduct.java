package saraye;

public class OrderProduct {
    int product_id;
    String product_name;
    int price;
    int quantity;

    public int getProduct_id() {
        return product_id;
    }

    public String getProduct_name() {
        return product_name;
    }

    public int getPrice() {
        return price;
    }

    public int getQuantity() {
        return quantity;
    }
}
